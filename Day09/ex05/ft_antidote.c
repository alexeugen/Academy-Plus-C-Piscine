void	ft_swap(int *a, int *b)
{
	*a = *a + *b;
	*b = *a - *b;
	*a = *a - *b;
}

int		ft_antidote(int i, int j, int k)
{
	if (i > j)
		ft_swap(&i, &j);
	if (i > k)
		ft_swap(&i, &k);
	if (j > k)
		ft_swap(&j, &k);
	return (j);
}
